/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  // --- VEML7700 Initialization ---

  // Configuration Value: 0x1000
  // ALS_GAIN = 1/8 (Bits 11:12 = 10) -> Good for outdoors/bright light
  // ALS_IT = 100ms (Bits 6:9 = 0000)
  // ALS_SD = 0 (Bit 0 = 0) -> Power On
  i2c_buf[0] = 0x00; // LSB
  i2c_buf[1] = 0x10; // MSB

  // Write Configuration to Register 0x00
  if (HAL_I2C_Mem_Write(&hi2c1, VEML7700_ADDR, ALS_CONF, I2C_MEMADD_SIZE_8BIT, i2c_buf, 2, 100) != HAL_OK)
  {
      strcpy(msg, "Error: I2C Init Failed. Check Wiring.\r\n");
      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
  }
  else
  {
      strcpy(msg, "VEML7700 Initialized Successfully.\r\n");
      HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
  }

  // Wait 4ms for sensor to power up (datasheet spec)
  HAL_Delay(10);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      // --- Read Data Loop ---

      // Read 2 bytes from ALS_DATA register (0x04)
      if (HAL_I2C_Mem_Read(&hi2c1, VEML7700_ADDR, ALS_DATA, I2C_MEMADD_SIZE_8BIT, i2c_buf, 2, 100) == HAL_OK)
      {
          // Combine bytes: High byte << 8 | Low byte
          als_raw = (i2c_buf[1] << 8) | i2c_buf[0];

          // Calculate Lux
          // Resolution Factor for Gain 1/8 and IT 100ms is 0.5760 (per datasheet)
          lux_val = als_raw * 0.5760f;

          // Format for UART printing (Integer workaround for standard printf support)
          int lux_int = (int)lux_val;
          int lux_dec = (int)((lux_val - lux_int) * 100);

          sprintf(msg, "Raw: %u | Lux: %d.%02d\r\n", als_raw, lux_int, lux_dec);
          HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
      }
      else
      {
          strcpy(msg, "Error reading sensor data.\r\n");
          HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), 100);
      }

      HAL_Delay(500); // 500ms delay between reads

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

